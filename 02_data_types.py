# python supports several data types 
# 1. int (integer) - whole numbers without a decimal point (e.g., 1, 42, -5)
# 2. float (floating-point) - numbers with a decimal point (e.g., 3.14, -0.001, 2.0)
# 3. str (string) - sequence of characters enclosed in quotes (e.g., "Hello", 'Python', "123")
# 4. bool (boolean) - represents truth values, either True or False
# 5. list - ordered collection of items (e.g., [1, 2, 3], ["apple", "banana", "cherry"])
# 6. tuple - ordered, immutable collection of items (e.g., (1, 2, 3), ("apple", "banana", "cherry"))
# 7. dict (dictionary) - collection of key-value pairs (e.g., {"name": "Alice", "age": 30}, {"fruit": "apple", "color": "red"})
# 8. set - unordered collection of unique items (e.g., {1, 2, 3}, {"apple", "banana", "cherry"})    

age = 20
print(age)
print(type(age)) #to check the data type of a variable

cgpa = 8.5
print(cgpa)
print(type(cgpa))

name = "Ayush"
print(name)
print(type(name))

